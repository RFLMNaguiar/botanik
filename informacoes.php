                <h2>Introdução</h2>
                <p>O ensino de botânica no ensino médio sempre foi um tema desafiador para alunos e professores da educação básica. Por se tratar de um tema muito teórico, com vocábulos difíceis e distante do cotidiano dos alunos, a botânica é considerada matéria escolar árida, entediante e fora do contexto moderno (SALATINO; BUCKERIDGE, 2016). Essa incapacidade de reconhecer o papel e valorizar as plantas, fenômeno chamado “cegueira botânica” (WANDERSEE; SCHUSSLER, 2001), causa grande consternação, em razão do papel fundamental que as plantas exercem no fluxo de energia e nutrientes, na regulação do clima, bem como no lazer e subsistência humana (CHACOURT, 1974; RIZZINNI; MORS, 1995).</p>
                <p>Uma forma de estimular o processo de ensino-aprendizagem de botânica na educação básica seria a atualização de ferramentas metodológicas que aproximem o ensino da realidade dos alunos. Com o advento e vulgarização de novas tecnologias, a maioria dos alunos dessa geração tem uma predileção por jogos, principalmente os digitais, usando parte de seu tempo diário com essas atividades (GROS, 2013). Dispositivos móveis, como tablets e smartphones, podem ser grandes aliados no desenvolvimento de habilidades e perspectivas, provendo, também, o conhecimento (SHULER, 2009; SUN, 2008). A popularização dos smartphones permitiu o desenvolvimento de aplicativos, não apenas voltados para o entretenimento do utilizador, mas também para fins educativos. O desenvolvimento de estratégias educacionais que permitam atrelar a aprendizagem à dinâmica dos jogos permite o desenvolvimento de aulas mais atrativas e motivadoras, possibilitando uma aprendizagem mais ativa e inovadora. Para Morán (2015), a educação ruma cada vez mais os ambientes virtuais e espaços que não sejam somente os das salas de aula, uma vez que isso “descentralizará o professor, para incorporar o conceito de que todos aprendemos juntos, de que a inteligência é mais coletiva, com múltiplas fontes de informação”.</p>
                <p>Ajudar os alunos a se tornarem sujeitos ativos no processo de aprendizagem é papel fundamental desse novo professor do século XXI e o uso dessas metodologias ativas, como instrumento para facilitar o aprendizado, usando de mecanismos que mudem e transformem o modo de aprender, pode ser pensado como uma forma de diminuir a dificuldade de compreensão do aluno na sala de aula. Ensinar de forma ativa depende do interesse e motivação do alunado, sugerindo o uso de recursos que façam parte do dia-a-dia do estudante e da colaboração de todos os integrantes do sistema educacional na sua implementação. Para Vygotsky (2007), “a criança tem um papel ativo no processo de aprendizagem, entretanto não atua sozinha. Ela aprende a pensar criando, sozinha ou com a ajuda de alguém, e interiorizando progressivamente versões mais adequadas das ferramentas “intelectuais” que lhe apresentam e lhe ensinam ativamente os adultos a sua volta”. </p>
                <p>A presente proposta se enquadra nas seguintes áreas temáticas extensionistas previstas no Plano Nacional de Extensão Universitária: “educação” e “meio ambiente”. Ao longo do desenvolvimento da presente proposta, as seguintes metas deverão ser cumpridas:</p>
                <ol>
                    <li>Desenvolvimento do aplicativo educacional;</li>
                    <li>Realização de oficinas para capacitação dos professores;</li>
                    <li>Avaliação do conhecimento dos alunos em botânica;</li>
                    <li>Uso do aplicativo educacional em sala de aula;</li>
                    <li>Avaliação da experiência dos alunos e professores ao utilizar o aplicativo educacional;</li>
                    <li>Análise dos dados obtidos;</li>
                    <li>Participação em eventos científicos;</li>
                    <li>Divulgação e distribuição do aplicativo educacional para demais professores de ensino médio interessados em adotar tal ferramenta em suas aulas.</li>
                </ol>
                <p><em>Nosso produto principal a ser desenvolvido é um <strong>aplicativo educacional</strong>, que consiste em um jogo de perguntas e respostas (quiz) que abordam os temas botânicos aprendidos no ensino médio (morfologia vegetal). Esperamos que o ambiente de jogo, propiciado ao utilizar o aplicativo, estimule os alunos a aprender botânica, melhorando o ensino desse conteúdo no ensino médio.</em></p>
